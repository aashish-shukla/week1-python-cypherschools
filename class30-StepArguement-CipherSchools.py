#slicing /selecting subsequences
lang = "python"
#syntax - [start arguement: stop arguement:step]
print("Aashish"[2:5])     #output= shi
print("Aashish"[2:5:1])   #output= shi
print("Aashish"[2:5:2])   #output= si  
print("Aashish"[0::2])    #output= asih
print("Aashish"[8::-1])   #output = hsihsaA
print("Aashish"[::-1])    #output = hsihsaA(TRICK)